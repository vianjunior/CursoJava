package cap5;

public class Calculo {
	
	double somar(int num1, int num2) {
		return num1 + num2;
	}
	
	double subtrair(int num1, int num2) {
		return num1 - num2;
	}
	
	double multiplicar(int num1, int num2) {
		return num1 * num2;
	}
	
	double dividir(int num1, int num2) {
		return num1 / num2;
	}

}
