package cap2;

public class DeclaracaoDeVariaveis {
	public static void main(String[] args) {
		int numero = 10;
		String texto = "Junior";
		char c = 'a';
		double decimal1 = 10.5;
		
		System.out.println("Numero = " + numero);
		System.out.println("Texto = " + texto);
		System.out.println("Caractere = " + c);
		System.out.println("Decimal = " + decimal1);
	}

}
