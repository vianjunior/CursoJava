package cap3;

public class OperadoresLogicos {

	public static void main(String[] args) {
		System.out.println("resultado usando and: "+ (10 == 3 && 8 != 10));
		System.out.println("resultado usando and: "+ (10 > 3 && 8 < 10));
		System.out.println("resultado usando or: "+ (10 < 3 || 8 == 10));
		System.out.println("resultado usando or: "+ (10 > 3 || 8 > 10));

	}

}
