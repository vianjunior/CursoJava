package cap3;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 2;
		
		System.out.println(a + " + " + b + " = " + (a + b));
		System.out.println(a + " - " + b + " = " + (a - b));
		System.out.println(a + " * " + b + " = " + (a * b));
		System.out.println(a + " / " + b + " = " + (a / b));

	}

}
